# String adalah tipe data yang termasuk sequence / sekuensial, karena pada dasarnya string adalah
# gabungan dari beberapa karakter / sequence of character

rabbit = "Jacko Sulaiman"

# Index dalam string rabbit
# J = 0
# a = 1
# c = 2
# dan seterusnya

print(rabbit)

# Mengambil index ke dua dari string, urutan ke tiga, yaitu c
print(rabbit[2])

# Mengambil index terakhir dari string, urutan terakhir yaitu n
print(rabbit[-1])