# Numeric Operators
a = 24
b = 4

print(a + b) # Pertambahan
print(a - b) # Pengurangan
print(a * b) # Perkalian
print(a / b) # Pembagian yang menghasilkan float
print(a // b) # Pembagian yang menghasilkan integer, dibulatkan kebawah
print (a % b) # Modulus
